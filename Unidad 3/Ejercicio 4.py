print("----- Suma Constante con Exit 0 -----")

User_Num_Init = 0
User_Num_Sec = int(input("Ingrese un numero para Sumarlo: "))

while User_Num_Sec != 0:
    User_Num_Init += User_Num_Sec
    print(User_Num_Init)
    User_Num_Sec = int(input("Ingrese un numero para Sumarlo: "))
print("----- Fin , Usted Salio Precionando 0 -----")
